package class2;

public class Numbers {
    public static void main(String[] args) {
        int num1=20;
        int num2=10;
        int addTotal=num1+num2;
        int subTotal=num1-num2;
        int mulTotal=num1*num2;
        int divTotal=num1/num2;
        System.out.println("The add of 2 numbers "+num1+" and "+num2+" is equal to "+addTotal);
        System.out.println("The sub of 2 numbers "+num1+" and "+num2+" is equal to "+subTotal);
        System.out.println("The mul of 2 numbers "+num1+" and "+num2+" is equal to "+mulTotal);
        System.out.println("The div of 2 numbers "+num1+" and "+num2+" is equal to "+divTotal);


    }
}
